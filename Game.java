package com.blackjack;

public class Game {
	
	private int playerTotal;
	private int houseTotal;
	private int playerCard;
	private int houseCard;
	private int firstPlayerCard;
	private int secondPlayerCard;
	private String displayCard;
	private int firstHouseCard;
	private int secondHouseCard;
	
	public int getPlayerTotal() {
		return playerTotal;
	}
	public void setPlayerTotal(int playerTotal) {
		this.playerTotal = playerTotal;
	}
	public int getHouseTotal() {
		return houseTotal;
	}
	public void setHouseTotal(int houseTotal) {
		this.houseTotal = houseTotal;
	}
	public int getPlayerCard() {
		return playerCard;
	}
	public void setPlayerCard(int playerCard) {
		this.playerCard = playerCard;
	}
	public int getHouseCard() {
		return houseCard;
	}
	public void setHouseCard(int houseCard) {
		this.houseCard = houseCard;
	}
	public int getFirstPlayerCard() {
		return firstPlayerCard;
	}
	public void setFirstPlayerCard(int firstPlayerCard) {
		this.firstPlayerCard = firstPlayerCard;
	}
	public int getSecondPlayerCard() {
		return secondPlayerCard;
	}
	public void setSecondPlayerCard(int secondPlayerCard) {
		this.secondPlayerCard = secondPlayerCard;
	}
	public String getDisplayCard() {
		return displayCard;
	}
	public void setDisplayCard(String displayCard) {
		this.displayCard = displayCard;
	}
	public int getFirstHouseCard() {
		return firstHouseCard;
	}
	public void setFirstHouseCard(int firstHouseCard) {
		this.firstHouseCard = firstHouseCard;
	}
	public int getSecondHouseCard() {
		return secondHouseCard;
	}
	public void setSecondHouseCard(int secondHouseCard) {
		this.secondHouseCard = secondHouseCard;
	}
	
}
